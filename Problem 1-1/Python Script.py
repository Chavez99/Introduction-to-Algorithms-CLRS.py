from math import *
N=1
n=1

t= 3.16*pow(10,15) 
while n * log(n, 2) < t:

    N += 1 
    def findFactSum(N):
        f = 1
        Sum = 0
        for i in range(1, N + 1):
            f = f * i
            Sum += f
        return Sum
    n = findFactSum(N)

while n * log(n, 2) > t:
   
    n -= 1
    while n * log(n, 2) > t:
        s=7
        n-=pow(10,s)
        #s+=1
#Scientific Notation
print("{:e}".format(n))

